      <tbody><tr><td id="m1356" class="tsub">Moord / dood</td></tr>
      <tr><td>
        <p>Een logisch gevolg van aanvallen kan de dood zijn. Wanneer het gezondheidspercentage van jouw tegenstander gezakt is tot het nulpunt, heb je de tegenstander vermoord (wanneer je eigen gezondheid tot het nulpunt zakt ben je dus ook dood!).</p>
        <p>Als een vermoord persoon een veiling had lopen, stopt deze veiling automatisch en krijgt de hoogste bieder zijn geld terug.</p>
        <p>
          <strong>Wanneer je dood bent verlies je het volgende:</strong><br>
          - Bank en contant geld (50% gaat naar je huwelijkspartner, indien getrouwd, het overige naar de moordenaar)<br>
          - Objecten in bezit (zoals ziekenhuizen, casino's etc.)
        </p>
        <p>Heb je zelf iemand vermoord? Dan krijg jij al zijn / haar  bank en contant geld. Wanneer je tegenstander getrouwd was krijg je "slechts" 50% van het bank- en contant geld.</p>
        <p><strong>Tip:</strong> Bewaar geld in een kluis, ook wanneer je vermoord bent blijft je geld daar veilig. Credits en VIP-lidmaatschap dagen gaan overigens niet verloren na een moord, deze houd je gewoon</p>
        <p>Nadat je dood bent kun je niet meer verder spelen. Je kan alleen uitloggen, credits kopen of je leven terugkopen in het wisselkantoor. Nadat je jezelf weer tot leven hebt gewekt kun je weer verder spelen.</p>
        <p>Om het voor iedereen leuk te houden kun je niet non-stop aanvallen. Na een moord kun je 60 minuten lang niemand aanvallen.</p>
      </td></tr>
