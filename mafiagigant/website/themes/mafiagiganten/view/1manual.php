      <tbody><tr><td id="m1096" class="tsub">Aanvallen</td></tr>
      <tr><td>
        <p>Door andere spelers aan te vallen kun je extra geld bemachtigen. Bij een succesvolle aanval verliest je tegenstander namelijk een gedeelte van het contant geld. Daarnaast verliest een persoon ook nog eens gezondheid. Er zit echter een maximum op het aanvallen. Je kan een persoon maximaal 2 keer per 24 uur onder vuur nemen, en in totaal kun je dagelijks 100 aanvallen uitvoeren op medespelers.</p>
        <p>Hoe meer kogels je afvuurt, hoe meer kans op winst en extra health loss bij je tegenstander. Zekerheid op succes heb je nooit. Wel is één ding zeker: hoe hoger je power en rank, hoe meer kans op een succesvolle aanval.</p>
        <p>Naast voordelen zitten er ook nadelen aan het aanvallen van tegenstanders: een aanval kost kracht (power). Kies dus zorgvuldig je slachtoffers uit. Wanneer een aanval niet succesvol was, zakt je eigen gezondheid i.p.v. de gezondheid van je tegenstander.</p>
        <p>
          Het is ook mogelijk dat je jouw tegenstander niet kan aanvallen, dit kan de onderstaande oorzaken hebben:<br>
          - Je staat onder bescherming<br>
          - Je tegenstander staat onder bescherming<br>
          - Je tegenstander is op vakantie<br>
          - Je hebt al 100 keer aangevallen vandaag, of je hebt deze gebruiker al 2 keer aangevallen<br>
          - Je bent aan het werk<br>
          - Je hebt het laatste uur iemand vermoord en moet dus wachten tot je weer toe kan slaan
        </p>
      </td></tr>
