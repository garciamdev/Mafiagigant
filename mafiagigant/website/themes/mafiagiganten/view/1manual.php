      <tr><td>
        <p>
          Poker is een spel waarbij je moet inzetten op basis van de kaarten die je in je handen hebt.
          Het spel dat hier gespeeld wordt is de variant Texas-Holdem.
        </p>
        <p>
          <strong>De blinds</strong><br>
          Voordat een spel begonnen kan worden moeten enkele verplichte inzetten worden gedaan: de blinds.
          Bij Texas-Holdem wordt een button gebruikt (de dealerbutton) om te laten zien welke speler moet delen.
          Voordat er kaarten kunnen worden gedeeld moet er door de spelers die links van de dealerbutton zitten verplicht de kleine blind en de grote blind (twee keer de kleine blind) inzetten.
          Wanneer dat gebeurd is krijgt iedere speler twee gesloten kaarten.
        </p>
        <p>
          <strong>Inzetopties</strong><br>
          De mogelijkheden die een speler heeft bestaan uit: "fold", "check", "bet", "call" en "raise".
          Niet alle acties zijn beschikbaar elke beurt, het hangt af van de acties die door de vorige spelers zijn gedaan.
          Een optie die altijd beschikbaar is, is "fold", de speler gooit dan zijn kaarten weg en speelt niet meer mee in die ronde.
          Wanneer er nog geen één speler heeft ingezet kan een speler checken (niets inzetten maar wel doorspelen) of betten.
          Wanneer er een speler wel heeft gebet dan hebben de spelers daarna de keuze om te callen, raisen of folden.
          Als een speler called dan betekend dat, dat een speler meegaat en evenveel inzet als de speler die heeft gebet.
          Als je kiest om te raisen dan call je de bet en verhoog je ook nog eens je inzet.
        </p>
        <p>
          <strong>Pre-Flop</strong><br>
          Wanneer alle spelers hun kaarten hebben gekregen krijgen ze de mogelijkheid om de grote blind te callen, raisen of te folden.
          De speler naast de grote blind moet als eerst actie ondernemen.
          Als hij called moet hij de big blind (laten we zeggen dat de grote blind bijvoorbeeld 10 is) callen.
          Zo gaat de hele tafel af totdat iedereen hetzelfde heeft ingezet.
        </p>
        <p>
          <strong>De Flop</strong><br>
          Bij de flop worden er drie kaarten open op tafel gelegd.
          Deze kaarten zijn voor alle spelers beschikbaar die nog in de ronde zitten.
          Degene die als eerst een actie moet uitvoeren is degene die links naast de dealerbutton zit.
          De opties zijn hetzelfde als bij de preflop, als er echter nog niet is ingezet mag er ook worden gechecked.
        </p>
        <p>
          <strong>De Turn</strong><br>
          Wanneer alle bets zijn geplaatst bij de flop ronde wordt de "turn" op tafel gelegd.
          Dit is de vierde kaart die tevens voor iedereen beschikbaar is.
          Er volgt daarna weer een inzetronde die net zoals bij de flop begint bij de speler naast de dealerbutton.
        </p>
        <p>
          <strong>De River</strong><br>
          Wanneer alle bets zijn geplaatst bij de "turn" komt de vijfde en ook meteen de laatste gemeenschappelijke kaart op tafel.
          Ook hier geldt weer; de speler naast de dealerbutton begint.
        </p>
        <p>
          <strong>Showdown</strong><br>
          Als alle bets zijn geplaatst bij de "river" worden alle kaarten opengelegd.
          De speler die met maximaal 5 kaarten de beste combinatie heeft wint de pot.
        </p>
        <p>
          <strong>Combinaties</strong><br>
          Bij poker worden de volgende kaartcombinaties onderscheiden, geordend van minst naar meest waarschijnlijk, tegelijk ook van meest naar minst waardevol.
        </p>
